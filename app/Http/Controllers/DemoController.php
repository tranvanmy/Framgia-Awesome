     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newUser = [
            'id' => '4',
            'first_name' => $request->input('first_name'),
            'last_name' => $request->input('last_name'),
            'user_name' => $request->input('user_name')
        ];

        if (!Session::has('key')) {
            $listUser = \config('user.list_user');
            array_push($listUser, $newUser);
            Session::put('key', $listUser);
        } else {
            $arraySession = Session::get('key');
            array_push($arraySession, $newUser);
            Session::put('key', $arraySession);
        }

        return response()->json(Session::get('key'));
    }

